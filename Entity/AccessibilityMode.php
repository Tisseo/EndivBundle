<?php

namespace Tisseo\EndivBundle\Entity;


/**
 * AccessibilityMode
 */
class AccessibilityMode
{
    const MODE_PMR = "UFR";
    const MODE_WALK = "Pietons";

    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $name;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param  string $name
     * @return AccessibilityMode
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}
